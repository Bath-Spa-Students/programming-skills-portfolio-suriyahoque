# Assuming the user has three courses with scores 85, 92, and 78
num_courses = 3
course1_marks = 85
course2_marks = 92
course3_marks = 78

# Calculate total marks and average
total_marks = course1_marks + course2_marks + course3_marks
average_marks = total_marks / num_courses

# Assuming total marks is 500
total_marks_possible = 500

# Calculate percentage
percentage = (total_marks / total_marks_possible) * 100

# Print results
print(f"\nAverage Marks: {average_marks}")
print(f"Percentage: {percentage}%")
