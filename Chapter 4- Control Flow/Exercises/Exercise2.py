# Version where the if block runs
alien_color = 'green'

# Check if the alien's color is green
if alien_color == 'green':
    # If true, print a message about earning 5 points
    print("Congratulations! You just earned 5 points for shooting the green alien.")
else:
    # If false, print a message about earning 10 points
    print("Nice shot! You just earned 10 points.")

